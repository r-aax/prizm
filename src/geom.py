class Vector:

#---------------------------------------------------------------------------------------------------

    def __init__(self, t = (0.0, 0.0, 0.0)):
        """
        Constructor.

        Arguments:
            t -- tuple of coordinates.
        """

        (x, y, z) = t
        self.X = x
        self.Y = y
        self.Z = z

#---------------------------------------------------------------------------------------------------

    def ToString(self):
        """
        Convert to string.

        Result:
            String.
        """

        return '(%f, %f, %f)' % (self.X, self.Y, self.Z)

